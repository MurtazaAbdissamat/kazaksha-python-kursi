"""

Мәліметтердің типін өзгерту бойынша тапсырма орындайық

    Шеңбердің ауданын табыңыз:  S = πR**2
    Шеңбердің периметрін табыңыз: P = 2πR
    Шеңбердің радиюсын қолданушыдан алайық
    π = 3.14 мәнін қолданыңыз


"""
pi = 3.14
r = float(input("Radius endir: "))
S = pi * (r ** 2)
print(type(S))
P = 2 * pi * r
print(type(P))
print("Audani = " + str(S) + " Perimetri = " + str(P))
